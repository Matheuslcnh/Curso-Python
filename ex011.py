largura = float(input('Digite a largura da parede: '))
altura = float(input('Digite a altura da parede: '))
area = largura * altura
litros = area / 2

print(f'LARGURA: {largura} m\nALTURA: {altura} m')
print(f'Sua area é {area }m²')
print(f'Total de tinta para pintar será de {litros} litros de tinta')